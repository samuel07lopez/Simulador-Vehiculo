    private void bAcelerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAcelerarActionPerformed
        // TODO add your handling code here:
        try{
            String intensidad = JOptionPane.showInputDialog(this, "Por favor, ingresa la intensidad: ");
            String[] mensaje = simulador.acelerar(Double.parseDouble(intensidad));
            if(mensaje[0].equals("El auto ha acelerado")){
                mostrarVelocidad();
                tTablero.setText(mensaje[0]);
            }
            else{
                tTablero.setText(mensaje[1]);
                JOptionPane.showMessageDialog(this, mensaje[0], "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            if(simulador.isAccidentado()){
                cambiarIconoDeLabel(Auto, "/home/sam/NetBeansProjects/Simulador/SimuladorAutomovil/src/SimuladorAutomovil/img/auto_accidentado.png");
            }
            else if(!simulador.isEncendido()){
                cambiarIconoDeLabel(Auto, "/home/sam/NetBeansProjects/Simulador/SimuladorAutomovil/src/SimuladorAutomovil/img/auto.png");
            }
            else{
                cambiarIconoDeLabel(Auto, "/home/sam/NetBeansProjects/Simulador/SimuladorAutomovil/src/SimuladorAutomovil/img/auto_acelerando.png");
            }
        }
        catch(NullPointerException e){
            System.out.println("Cerraste el dialogo o algo así");
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Debes ingresar un numero valido", "ERROR", JOptionPane.ERROR_MESSAGE);
            bAcelerarActionPerformed(evt);
        }
    }//GEN-LAST:event_bAcelerarActionPerformed

