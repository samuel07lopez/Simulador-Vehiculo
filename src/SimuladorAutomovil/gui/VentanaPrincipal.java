    //este metodo se utiliza para cambiar el icono de un label usando la ruta de la imagen
    public static void cambiarIconoDeLabel(JLabel label, String rutaImagen) {
        try {
            // Cargar la imagen desde el archivo en la ruta especificada
            ImageIcon icono = new ImageIcon(rutaImagen);

            // Obtener el tamaño del JLabel
            int ancho = label.getWidth();
            int alto = label.getHeight();

            // Escalar la imagen al tamaño del JLabel
            Image imagen = icono.getImage();
            Image nuevaImagen = imagen.getScaledInstance(ancho, alto, Image.SCALE_SMOOTH);

            // Crear un ImageIcon a partir de la imagen escalada
            ImageIcon iconoEscalado = new ImageIcon(nuevaImagen);

            // Establecer el nuevo icono en el JLabel
            label.setIcon(iconoEscalado);
        } catch (Exception e) {
            e.printStackTrace();
            // Manejar cualquier excepción que pueda ocurrir al cargar la imagen
            JOptionPane.showMessageDialog(null, "Error al cargar la imagen", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

