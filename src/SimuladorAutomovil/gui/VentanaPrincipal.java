    private void bEncenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEncenderActionPerformed
        // TODO add your handling code here:
        String mensaje = simulador.enceder();
        if(mensaje.equals("Su auto ha encendido")){
            mostrarVelocidad();
            tTablero.setText(mensaje);
        }
        else{
            mostrarVelocidad();
            JOptionPane.showMessageDialog(this, mensaje, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        if(simulador.isAccidentado()){
            cambiarIconoDeLabel(Auto, "/home/sam/NetBeansProjects/Simulador/SimuladorAutomovil/src/SimuladorAutomovil/img/auto_accidentado.png");
        }
        else{
            cambiarIconoDeLabel(Auto, "/home/sam/NetBeansProjects/Simulador/SimuladorAutomovil/src/SimuladorAutomovil/img/auto_encendido.png");
        }
    }//GEN-LAST:event_bEncenderActionPerformed

